//Ділення та залишок від ділення
public class lab21 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        int division = a / b;  // Цілочисельне ділення
        int remainder = a % b; // Остача від ділення

        System.out.println("Результат ділення: " + division);
        System.out.println("Остача від ділення: " + remainder);
    }
}

